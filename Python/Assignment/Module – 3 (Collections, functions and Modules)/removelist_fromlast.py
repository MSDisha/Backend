#  How will you remove last object from a list?  Suppose list1 is [2, 33, 222, 14, and 25], what is list1 [-1]? 
list=[2, 33, 222, 14,25]
list1 =list[:-1]
print(list1)

# The expression list1[-1] returns the last element of the list. In Python, negative indexing allows you to access elements from the end of the list.