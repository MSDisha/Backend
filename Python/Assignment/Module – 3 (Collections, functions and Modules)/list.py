#What is List? How will you reverse a list?  
# A list in Python is a collection data type that can store multiple items in a single variable. It is ordered, mutable (modifiable), and allows duplicate elements. Lists are defined using square brackets [], and the items can be of any data type (integers, strings, floats, or even other lists).

my_list = [1, 2, 3, 4, 5]
reversed_list = my_list[::-1]
print(reversed_list) 
